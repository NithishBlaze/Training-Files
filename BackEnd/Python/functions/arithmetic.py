def add(num1,num2):
    result = num1 + num2
    return result
def sub(num1,num2):
    result = num1 - num2
    return result
def mul(num1,num2):
    result = num1 * num2
    return result
def div(num1,num2):
    result = num1 / num2
    return result
def floorDiv(num1,num2):
    result = num1 // num2
    return result
def expo(num1,num2):
    result = num1 ** num2
    return result